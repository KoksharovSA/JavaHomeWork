public class Point {
    int X;
    int Y;
    int number;
    /**
     * 
     * @param x координата int[][x]
     * @param y координата int[y][]
     * @param number значение в ячейке
     */
    public Point(int x, int y, int number) {
        X = x;
        Y = y;
        this.number = number;
    } 
}
